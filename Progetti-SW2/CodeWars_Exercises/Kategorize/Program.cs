using System;
using System.Collections.Generic;
using System.Linq;
using Microsoft.VisualBasic;

public class Kata
{
    public static IEnumerable<string> OpenOrSenior(int[][] data)
    {
        foreach(var a in data[a][]&& var h in data[][h])
            if (a > 54 && h >= 7)
                yield return "Senior";
            else
                yield return "Open";
    }
}